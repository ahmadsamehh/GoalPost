import UIKit

extension UIButton{
    
    func buttonSelectedColor(){
        
        self.backgroundColor = #colorLiteral(red: 0, green: 0.5628422499, blue: 0.3188166618, alpha: 0.9671179366)
        
    }
    
    func buttonDeselectedColor(){
        
        self.backgroundColor = #colorLiteral(red: 0.6980392157, green: 0.8666666667, blue: 0.6862745098, alpha: 0.9671179366)
        
    }
    
    
}
